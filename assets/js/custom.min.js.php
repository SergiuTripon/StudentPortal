function compress($minify) {

    /* remove comments */
    $minify = preg_replace("/((?:\/\*(?:[^*]|(?:\*+[^*\/]))*\*+\/)|(?:\/\/.*))/", "", $minify);

    /* remove tabs, spaces, newlines, etc. */
    $minify = str_replace(array("\r\n","\r","\t","\n",'  ','    ','     '), '', $minify);

    $minify = preg_replace(array('(( )+\))','(\)( )+)'), ')', $minify);

    return $minify;
}

